def emit(name: str, rows: list[str]) -> list[str]:
    alpha: list[int] = []
    for row in rows:
        for ch in row.ljust(SZ)[:SZ]:
            if ch == ".":
                alpha.append(0)
            elif ch == "-":
                alpha.append(80)
            elif ch == "+":
                alpha.append(200)
            else:
                alpha.append(255)
    lines = [f"static const uint8_t SPR_{name}_A[{SZ * SZ}] = {{"]
    for i in range(0, len(alpha), SZ):
        chunk = ", ".join(str(v) for v in alpha[i : i + SZ])
        lines.append(f"    {chunk},")
    lines.append("};")
    return lines


def main() -> None:
    parts = [
        "#pragma once",
        "#include <stdint.h>",
        f"#define GAME_SPRITE_SIZE {SZ}",
        "",
    ]
    for name, art in SPRITES.items():
        parts.extend(emit(name, art))
        parts.append("")
    open(OUT, "w", encoding="utf-8").write("\n".join(parts) + "\n")
    print(f"Wrote {OUT}")


if __name__ == "__main__":
    main()
